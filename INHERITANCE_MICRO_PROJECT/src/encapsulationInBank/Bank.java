package encapsulationInBank;

public class Bank {
	private String fName;
	private String lName;
	private long phoneNumber;
	private long accNo;
	private String ifceCode;
	public Bank(String fName, String lName, long phoneNumber, long accNo, String ifceCode) {
		super();
		this.fName = fName;
		this.lName = lName;
		this.phoneNumber = phoneNumber;
		this.accNo = accNo;
		this.ifceCode = ifceCode;
	}
	
	

}
